/*
 * 주제: 값 표현 => 리터럴(literal)
 * -. 정수, 실수, 논리, 문자열
 */
package step01;

public class Exam04  { 
  public static void main(String[] args) {
    
    System.out.println("Hello"); // 자바에서 문자열은 반드시 큰따옴표를 붙여야 함
    System.out.println();
    
    System.out.println(10);
    System.out.println(-20);
    System.out.println();
    
    System.out.println(3.141592);
    System.out.println(-27.56);
    System.out.println();
    
    System.out.println(true); // 대소문자를 구별함 반드시 소문자
    System.out.println(false);
    System.out.println();
    
  }
}
